def make_dough(ingredient1, ingredient2):
    if ingredient1 == 'water' and ingredient2 == 'flour':
        return 'dough'
    else:
        return 'Not dough'


def bake_bread(ingredient3):
    if ingredient3 == 'dough':
        return 'bread'
    else:
        return 'NOT bread'


def make_wheat_dough(ingredient1, ingredient4):
    if ingredient1 == 'water' and ingredient4 == 'wholewheat flour':
        return 'Wholewheat dough'
    else:
        return 'Not wholewheat dough'

